public class narrowing 
{
    public static void main(String[] args) 
    {
        // narrowing 
        int x=68;
        System.out.println("value of x intiger value is:"+x);
        char ch=(char)x;
        System.out.println("value of ch form interger value:"+ch);
        double d=8.3423;
        System.out.println("value of double d :"+d);
        float f=(float)d;
        System.out.println("value of f from double :"+f);
        int i=(int)f;
        System.out.println("value of i is from float:"+i);
       
    }
}
